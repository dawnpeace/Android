package com.example.dawnpeace.spota_android.Classes;

public class ParentComment {

    int id;
    String name;
    int number;

    public ParentComment(int id, String name, int number) {
        this.id = id;
        this.name = name;
        this.number = number;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getNumber() {
        return number;
    }
}
